package com.demo;

public class PreIncreDecreOperator {
	    public static void main(String[] args) {
	        int a=3;
	
	        System.out.println(++a);
	        System.out.println(++a);
	        a++;
	        a--;
	        
	        System.out.println(--a);
	        System.out.println(--a);
	        System.out.println(--a);
	        
	        System.out.println(a);
	    }
	}

