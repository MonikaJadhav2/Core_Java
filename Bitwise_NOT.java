package com.demo;

public class Bitwise_NOT {
	public static void main(String[] args) {
		
		
		        int a = 5;   // binary: 0101
		        int b = 3;   // binary: 0011
		        int c = 7;   //binary: 0111

		       

		        System.out.println("~ a= " + ~a );
		        System.out.println("~ b = " + ~b);
		        System.out.println("~ c = " + ~c);
		    }

}
